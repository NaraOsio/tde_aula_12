public class Calculadora
{
    public int somar(int a, int b)
    {
        return a + b;
    }
    public int somar(int a, int b, int c)
    {
        return a + b + c;
    }

    static int soma(int a, int b, int c, int d)
    {
        int resultado = a + b + c + d;
        return resultado;

    }
}